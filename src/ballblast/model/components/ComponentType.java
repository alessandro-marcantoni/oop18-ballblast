package ballblast.model.components;

/**
 * All types of {@link Component} which can be used.
 */
public enum ComponentType {
    /**
     * InputComponent.
     */
    INPUT,
    /**
     * MovementComponent.
     */
    MOVEMENT,
    /**
     * GravityComponent.
     */
    GRAVITY,
    /**
     * CollisionComponent.
     */
    COLLISION,
    /**
     * ShooterComponent.
     */
    SHOOTER,
    /**
     * SplitterComponent.
     */
    SPLITTER;
}
